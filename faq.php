<a id="projectionimages"></a><h2>How do I access the projection quality images?</h2><br />
<p>In most browsers, click on the button under the image thumbnail which says &quot;Copy image URL to clipboard&quot;. This will place the path to the full image onto your computer clipboard. Use your computer's keyboard shortcut for paste (Ctrl+V or Cmd+V) in the browser's navigation window and hit &quot;Enter&quot;. This will navigate you to the larger image.</p>

<p>Internet Explorer users have a different button which says &quot;Go to full image&quot;. This will navigate directly to the large projection image.</p>

<p>&nbsp;</p>

<a id="citing"></a><h2>How do cite the information here in my work?</h2><br />
<p>Many of our images are from books and magazines, and information about those sources can be found on the details page for each image. If you are using images from our special collections or cannot find source information on the details page, please contact a curator for help on citing.</p>

<p>&nbsp;</p>

<a id="archiveimages"></a><h2>The projection quality images are not high resolution enough. Could I access higher quality versions?</h2><br />
<p>Projection images are limited to 2400 pixels in either direction. The slide library usually has higher quality archival images available upon request. Please email a slide library curator or stop by our office hours!</p>

<p>&nbsp;</p>

<a id="errorreport"></a><h2>I found incorrect information or a bug in the database. How do I report this?</h2><br />
<p>Please email one any one of the curators to fix incorrect image or video information, such as wrong dates or misspelled names and places. Their emails are found at the bottom of each page.</p>

<p>If there is an error in website behavior or you are experiencing technical difficulties, please specifically contact the Assistant Curator for Visual Resources.</p>

<p>&nbsp;</p>

<a id="projectionwhy"></a><h2>Why is the process for projection images so roundabout?</h2><br />
<p>In order to reduce redundancy of storage, the projection images are not duplicated onto the server but are stored on a network drive at GSAPP. Most browsers will not allow a website to open up a file on a user's computer because of security concerns, but this security hole in Internet Explorer can be exploited to navigate to the image directly.</p>

